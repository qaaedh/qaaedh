# Requesting user input for three integers
print("Please enter three integers:")
a = int(input("Number 1: "))
b = int(input("Number 2: "))
c = int(input("Number 3: "))

# Calculating the sum, average, and product of the integers
sum_numbers = a + b + c
average_numbers = sum_numbers / 3

# Displaying the results
print("\nThe sum of the numbers is:", sum_numbers)
print("The average of the numbers is:", average_numbers)